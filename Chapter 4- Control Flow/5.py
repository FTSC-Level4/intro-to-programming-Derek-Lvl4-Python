# I Create a list of your three favorite fruits
favorite_fruits = ["apple", "banana", "strawberry"]

# I Check if specific fruits are in the list and print the statements
if "apple" in favorite_fruits:
    print("You really like apples!")

if "banana" in favorite_fruits:
    print("You really like bananas!")

if "strawberry" in favorite_fruits:
    print("You really like strawberries!")

if "orange" in favorite_fruits:
    print("You also enjoy oranges!")

if "kiwi" in favorite_fruits:
    print("Kiwi is another one of your favorites!")

#Explanation of the code:

#I created a list called favorite_fruits with your three favorite fruits: "apple," "banana," and "strawberry." 

#We use five independent if statements to check whether specific fruits are in your list of favorite fruits.

#Each if statement is independent, so it will check for the presence of a specific fruit and print a statement accordingly.

#When you run this code, it will check your list of favorite fruits and print statements based on the fruits found in the list.





